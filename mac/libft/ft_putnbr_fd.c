#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n >= 10 || n <= -10 || n == -2147483648)
		ft_putnbr_fd(n / 10, fd);
	else if (n < 0)
		write(fd, "-", 1);
	if (n == -2147483648)
		c = '8';
	else
		c = '0' + (((n >= 0) * n - (n < 0) * n) % 10);
	write(fd, &c, 1);
}
