#include "libft.h"

int	ft_isalpha(int c)
{
	int	b;

	if (c > 256)
		return (0);
	b = ('a' <= (char) c && (char) c <= 'z');
	return (b || ('A' <= (char) c && (char) c <= 'Z'));
}
